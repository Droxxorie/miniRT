#include <minirt_bonus.h>

t_aabb	compute_cone_bounds(void)
{
	t_aabb	box;

	box.min = (t_point3){-1.0, 0, -1.0};
	box.max = (t_point3){1.0, 1.0, 1.0};
	return (box);
}
