#include <minirt_bonus.h>

//* R = I - 2 * (I . N) * N
t_vec3	vec_reflect(t_vec3 i, t_vec3 n)
{
	return (vec3_sub(i, vec3_scale(n, 2.0 * vec3_dot(i, n))));
}
